        <summary>タグの分類について</summary>
        <p class="recommend-desc">
            各ルームには、そのルームのテーマを最も適切に表す1つのタグが自動で付けられます。例えば「美容室」の話題のルームには「美容室」のタグが付き、「美容」のタグは付きません。
        </p>
        <p class="recommend-desc">
            タグの分類方法は、キーワードの一致だけでなく、文の意味や異なる表現も自動的に考慮します。これにより、ルームの実際の内容に忠実なタグが付くので、興味関心にぴったり合ったルームを見つけやすくなります。
        </p>
        <p class="recommend-desc">
            目的のルームが見つからない場合は、関連のタグもチェックしてみましょう。
        </p>
    </details>
