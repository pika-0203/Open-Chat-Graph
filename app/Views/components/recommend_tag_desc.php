        <summary>タグの分類について</summary>
        <p class="recommend-desc">
            各ルームは、その内容を最も適切に表現する1つのタグで自動的に分類されます。例えば、美容室に関する話題のルームは「美容室」とタグ付けされ、「美容」とはタグ付けされません。 </p>
        <p class="recommend-desc">
            このタグの分類ロジックは、キーワードの一致だけでなく、文脈や異なる表現も考慮に入れて自動的に実行されます。これにより、ルームの内容を正確に反映したタグが付けられ、自分の興味や関心に合ったルームをより簡単に見つけることができます。 </p>
        <p class="recommend-desc">
            目的のルームを見つけられない場合は、関連する他のタグも探してみましょう。
        </p>
